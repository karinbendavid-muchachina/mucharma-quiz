export default async function handler(req, res) {
  if (req.method !== "POST") {
    return res.status(405).json({ error: "Method not allowed" });
  }

  const { q1, q2, q3, q4 } = req.body;

  if (!q1 || !q2 || !q3 || !q4) {
    return res.status(400).json({ error: "כל השאלות חייבות להיות מלאות" });
  }

  const userMessage = `
שאלה 1 — באיזה תחום את מרגישה את הפער הכי גדול?
${q1}

שאלה 2 — הערכים שמנחים את הגרסה המוגשמת שלך:
${q2}

שאלה 3 — מה ה"אבל" הכי גדול שמונע ממך?
${q3}

שאלה 4 — איזה כוח-על היית רוצה לאמץ?
${q4}
  `.trim();

  try {
    const model = genAI.getGenerativeModel({
      model: "gemini-1.5-flash",
      systemInstruction: SYSTEM_PROMPT,
    });

    const result = await model.generateContent(userMessage);
    const text = result.response.text();

    const jsonMatch = text.match(/\{[\s\S]*\}/);
    if (!jsonMatch) throw new Error("No JSON in response");

    const data = JSON.parse(jsonMatch[0]);
    res.status(200).json(data);
  } catch (error) {
    console.error(error);
    res.status(500).json({ error: "משהו השתבש — נסי שוב" });
  }
}
